# ------------------------------------------------------------------------------
# EJERCICIO 11: Generación de Números Pares con un Bucle While
# ------------------------------------------------------------------------------
# Instrucciones:
# - Establece una variable contador en 2.
# - Mientras el contador sea menor o igual a 20:
#     • Imprime el número
#     • Aumenta el contador en 2.
# - Asegúrate de que no se produzca un bucle infinito.
# ------------------------------------------------------------------------------

contador = 2

while contador <= 20:
    print(f"Número par: {contador}")
    contador += 2
